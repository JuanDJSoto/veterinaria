    void Llenar_medicamentos(){
        try {
            conn = Login.getConnection();
            String[] titulos ={"Folio","Nombre","Existencia"};
            String sql = "Select ID_medicamento,Nombre,Existencia from medicamentos";
            model = new DefaultTableModel(null, titulos);
            sent = conn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            String[] fila = new String[3];
            while (rs.next()){
                fila[0]=rs.getString("ID_medicamento");
                fila[1]=rs.getString("Nombre");
                fila[2]=rs.getString("Existencia");                
                model.addRow(fila);
            }
            jTable1.setModel(model);
            conn.close();
        } catch (Exception e){
            e.printStackTrace();
        }
        
    }
    
